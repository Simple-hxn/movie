public class SysCategory {
	/** 类型对象的唯一标识属性*/
	private Integer id;
	/** 类型名称*/
	private String name;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
